# 转义符	含义	效果示例
# \t	水平制表符（Tab 键）	输出一段空白缩进，占 4/8 字符位
# \n	换行符	光标跳到下一行
# \r	回车符	光标回到本行开头，覆盖前面文字
# \\	输出反斜杠 \	打印单独一个 \
# \'	转义单引号	单引号字符串里输出 '
# \"	转义双引号	双引号字符串里输出 "

# 转义符	作用
# \v	垂直制表符，换行垂直空格
# \f	换页符，分页打印用
# \0	空字符（ASCII 0），无可见内容
# \a	响铃，终端会蜂鸣提示
# \b	退格符，删除前一个字符