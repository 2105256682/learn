# 2. 带参数的函数：计算两个数的和
# a, b 是【形式参数】，用来接收外面传进来的值
def add(a, b):
    """计算两个数的和并返回结果"""
    result = a + b  # 函数内部计算
    return result   # return：把结果【返回】给调用者

# 调用函数，传入 10 和 20
# 10, 20 是【实际参数】
print("2 + 3 =", add(2, 3))

