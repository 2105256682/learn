# 4. 经典函数：求阶乘 (复习巩固)
def fac(num):
    """求 num 的阶乘"""
    result = 1
    # 循环从 2 开始累乘到 num
    for n in range(2, num + 1):
        result *= n
    # 返回最终计算结果
    return result

# 调用阶乘函数计算组合数
m = 5
n = 2
print(f"从{m}个中选{n}个的组合数是：", fac(m) // fac(n) // fac(m - n))

