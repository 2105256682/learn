# 5. 拓展：强制关键字参数 (* 的用法)
def print_info(name, *, age):
    """age 必须用 关键字=值 的形式传入"""
    print(f"姓名：{name}，年龄：{age}")

# 正确调用：age 必须写 age=
print_info('小明', age=20)

# 错误调用：下面这行会报错！因为 age 在 * 后面，必须用关键字
# print_info('小明', 20)