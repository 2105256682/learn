# 导入随机数模块，用来生成随机答案
import random

# ---------------------------
# 1. 游戏规则说明
# ---------------------------
print("-" * 60)
print("欢迎来到 《猜数游戏》")
print("规则一：系统每次会自动生成一个1~100之间的随机数")
print("规则二：每次游戏最多只能猜三次")
print("规则三：进入游戏或者继续玩，输入：yes或者y")
print("规则四：离开游戏，输入：no或者n")
print("-" * 60)

# ---------------------------
# 2. 主循环：控制游戏是否继续玩
# ---------------------------
while True:
    # 询问用户是否开始/继续游戏
    order = input("请输入是否开始游戏：").strip().lower()  # 去除空格并转小写，兼容大小写输入

    # 不想玩，退出游戏
    if order == 'no' or order == 'n':
        print("游戏结束，欢迎下次再来玩！")
        break

    # 想玩，开始一局游戏
    elif order == 'yes' or order == 'y':
        # 生成1~10之间的随机数（包括1和10）
        number = random.randint(1, 100)
        print("\n游戏开始！系统已生成1~100之间的随机数，你有3次机会！")

        # 最多猜3次，循环i从1到3（range(1,4)生成1,2,3）
        for i in range(1, 4):
            # 获取用户输入，并转为整数（input默认是字符串）
            my_num = int(input(f"第{i}次猜，请输入你猜的数字："))

            # 情况1：猜对了
            if my_num == number:
                print(f"恭喜你，猜中了，答案就是{my_num}！")
                break  # 猜对了，直接跳出循环，结束这一局

            # 情况2：猜大了
            elif my_num > number:
                print(f"你猜的数字太大了，还剩下{3 - i}次机会！")

            # 情况3：猜小了
            else:
                print(f"你猜的数字太小了，还剩下{3 - i}次机会！")

        # 三次机会用完都没猜对，公布答案
        else:
            print(f"很遗憾，3次机会用完了，正确答案是：{number}")

        print("-" * 40)  # 分隔线，一局结束

    # 输入了无效内容，提示用户
    else:
        print("输入无效，请输入 yes/y 开始游戏，或 no/n 退出游戏！")