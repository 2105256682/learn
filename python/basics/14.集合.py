# ---------------------- 1. 集合的三大特性 ----------------------
# 1. 无序性：没有顺序，不能用索引
# 2. 互异性：自动去重
# 3. 确定性：支持 in / not in 判断，查询极快

# ---------------------- 2. 创建集合（4种方式） ----------------------

# 方式1：使用 {} 创建（最常用）
# 重复元素会自动去重
set1 = {1, 2, 3, 3, 2, 1}
print("1. 字面量创建集合（自动去重）:", set1)  # {1,2,3}

# 方式2：使用 set() 构造器
# 空集合必须用 set()，不能用 {}（{} 是空字典）
empty_set = set()
print("2. 空集合:", empty_set)

# 把字符串转成集合（自动去重）
set2 = set("hello")
print("3. 字符串转集合:", set2)  # {'h','e','l','o'}

# 把列表转成集合
set3 = set([1, 2, 2, 3, 3, 3])
print("4. 列表转集合:", set3)

# 方式3：集合生成式
set4 = {x for x in range(1, 10) if x % 2 == 0}
print("5. 集合生成式:", set4)

# 注意：集合元素必须是 不可变/可哈希 类型
# 下面这行会报错！列表不能放进集合
# set_error = {[1,2], 3}

# ---------------------- 3. 遍历集合 ----------------------
# 集合无序，只能用 for 遍历
print("\n6. 遍历集合:")
lang_set = {"Python", "Java", "C++"}
for item in lang_set:
    print("   ", item)

# ---------------------- 4. 成员运算（in / not in） ----------------------
print("\n7. 成员运算:")
print("   2 在 set1 中?", 2 in set1)        # True
print("   5 不在 set1 中?", 5 not in set1)  # True

# ---------------------- 5. 集合数学运算（重点） ----------------------
a = {1, 2, 3, 4, 5, 6}
b = {4, 5, 6, 7, 8}

print("\n8. 集合二元运算:")
# 交集：两个集合都有的元素
print("   交集(&):", a & b)
print("   交集方法:", a.intersection(b))

# 并集：合并所有元素（去重）
print("   并集(|):", a | b)
print("   并集方法:", a.union(b))

# 差集：a 有，b 没有
print("   差集(-):", a - b)
print("   差集方法:", a.difference(b))

# 对称差：只在其中一个集合里的元素
print("   对称差(^):", a ^ b)
print("   对称差方法:", a.symmetric_difference(b))

# ---------------------- 6. 复合赋值运算 ----------------------
x = {1, 2, 3}
y = {3, 4, 5}
x |= y  # 等价 x = x | y（求并集并赋值）
print("\n9. 复合赋值并集:", x)

x &= y  # 求交集赋值
print("   复合赋值交集:", x)

# ---------------------- 7. 集合比较关系 ----------------------
s1 = {1, 2}
s2 = {1, 2, 3}
s3 = {1, 2, 3}

print("\n10. 集合比较:")
print("   s1 == s2?", s1 == s2)          # False
print("   s2 == s3?", s2 == s3)          # True
print("   s1 是 s2 子集?", s1 <= s2)     # True
print("   s1 是 s2 真子集?", s1 < s2)    # True
print("   s2 是 s1 超集?", s2 >= s1)     # True
print("   方法判断子集:", s1.issubset(s2))
print("   无交集?", s1.isdisjoint({7,8})) # True

# ---------------------- 8. 集合常用方法（增删改查） ----------------------
test_set = {10, 20, 30}
print("\n11. 集合方法:")

# 添加元素
test_set.add(40)
test_set.add(40)  # 重复添加无效
print("   添加后:", test_set)

# 删除元素
test_set.discard(20)  # 元素不存在也不报错
test_set.remove(30)   # 元素不存在会报错
print("   删除后:", test_set)

# 随机删除一个元素并返回
pop_item = test_set.pop()
print("   随机删除:", pop_item)

# 清空集合
test_set.clear()
print("   清空后:", test_set)

# ---------------------- 9. 不可变集合 frozenset ----------------------
# 不可变：不能 add / remove
# 可哈希：可以放进集合/字典
f_set = frozenset([1, 2, 3, 4])
print("\n12. 不可变集合:", f_set)

# 同样支持集合运算
print("   frozenset 交集:", f_set & {3,4,5})

