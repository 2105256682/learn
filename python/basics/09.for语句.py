# ---------------------------
# 题目：计算 1~100（不含100）所有奇数的和
# 正确结果：2500
# ---------------------------

# 方法1：range 步长直接取奇数（最简洁高效）
# range(1,100,2) → 1,3,5,...,99
my_sum = 0                     # 初始化累加和
for n in range(1, 100, 2):    # 直接生成所有奇数
    my_sum += n                 # 累加
print("方法1结果:", my_sum)    # 2500


# 方法2：遍历0~99，if 判断奇数
my_sum = 0                     # 初始化累加和
for n in range(100):          # 遍历0到99
    if n % 2 != 0:            # 不能被2整除 → 奇数
        my_sum += n            # 是奇数就累加
print("方法2结果:", my_sum)    # 2500


# 方法3：while True + break 控制循环
my_sum = 0                     # 初始化累加和
n = 0                          # 计数器初始值
while True:                    # 无限循环
    n += 1                     # 计数器+1
    if n > 100:                # 超过100就退出
        break
    if n % 2 != 0:            # 判断是否为奇数
        my_sum += n            # 是奇数就累加
print("方法3结果:", my_sum)    # 2500


# 方法4：for + continue 跳过偶数
my_sum = 0                     # 初始化累加和
for n in range(100):          # 遍历0到99
    if n % 2 == 0:            # 如果是偶数
        continue               # 跳过本次循环，进入下一次
    my_sum += n                # 剩下的就是奇数，累加
print("方法4结果:", my_sum)    # 2500