# 输出1-100里奇数和
s_sum = 0  # 定义总和变量，初始值为0
a = 1  # 定义循环变量，从1开始
while 1 <= a <= 100:  # 循环条件：a在1到100之间
    if a % 2 != 0:  # 判断：如果a是奇数（除以2不等于0）
        s_sum = s_sum + a  # 是奇数就加到总和里
    a = a + 1  # 每次循环a加1
print(s_sum)  # 输出最终结果
# 运行结果：2500


# 输出九九乘法表
i = 1  # 外层循环控制行数，从1到9
while i <= 9:
    j = 1  # 内层循环控制每行的列数
    while j <= i:
        print(f"{i} x {j} = {i * j}", end=" ")  # 打印乘法式，不换行
        j = j + 1  # 内层循环变量+1
    print()  # 一行打印完，换行
    i = i + 1  # 外层循环变量+1
